package tp01.ejercicio5;

public class Datos {
	private int min;
	private int max;
	private float prom;

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public double getProm() {
		return prom;
	}

	public void setProm(float prom) {
		this.prom = prom;
	}
}
